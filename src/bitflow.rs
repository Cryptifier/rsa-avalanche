    fn bits_from_str(bits: &str) -> Vec<bool> {
        bits.chars().map(|c| c == '1').collect()
    }

    fn bits_to_string(bits: &[bool]) -> String {
        bits.iter()
            .map(|bit| if *bit { '1' } else { '0' })
            .collect()
    }

    fn accuracy_pct(candidate: &[bool], target: &[bool]) -> f64 {
        let matches = candidate
            .iter()
            .zip(target.iter())
            .filter(|(a, b)| a == b)
            .count();
        100.0 * matches as f64 / target.len() as f64
    }

    fn avalanche_reduce(candidates: &[Vec<bool>]) -> Vec<bool> {
        let nodes: Vec<AvalancheNode> = candidates
            .iter()
            .map(|bits| AvalancheNode {
                biases: vec![0.0; bits.len()],
                message_bits: bits.clone(),
            })
            .collect();
        search_avalanche_tree(nodes)
            .expect("avalanche search should succeed")
            .message_bits
    }

    #[test]
    fn test_bitflow_linear_progression_expected_candidates() {
        let config = BitflowConfig {
            bit_width: 8,
            min_partition_size: 2,
            max_partition_size: 4,
            progression: PartitionProgression::Linear { start: 2, step: 1 },
            max_iterations: 2,
            max_partitions_to_flip: 2,
            per_candidate_trials: 2,
            seed: 7,
            pow_mod_base: 5,
            pow_mod_modulus: 7,
        };
        let mut engine = BitflowEngine::new(config).expect("valid config");
        let message = bits_from_str("11001100");
        let candidates = engine.generate_candidates(&message).expect("candidates");
        let candidate_bits: Vec<String> = candidates
            .iter()
            .map(|cand| bits_to_string(&cand.bits))
            .collect();
        let expected = vec!["11110000", "00001100", "11001111", "00101111"];
        assert_eq!(candidate_bits, expected);
        let avalanche_bits = avalanche_reduce(
            &candidates
                .iter()
                .map(|c| c.bits.clone())
                .collect::<Vec<_>>(),
        );
        assert_eq!(bits_to_string(&avalanche_bits), "00000000");
    }

