/// Errors returned by the bitflow generator.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum BitflowError {
    InvalidBitWidth,
    InvalidPartitionRange,
    InvalidIterations,
    InvalidTrials,
    InvalidFlipCount,
    InvalidPowModulus,
    MessageBitWidthMismatch,
}

impl std::fmt::Display for BitflowError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            BitflowError::InvalidBitWidth => write!(f, "bit width must be non-zero"),
            BitflowError::InvalidPartitionRange => {
                write!(f, "partition sizes must be within the bit width")
            }
            BitflowError::InvalidIterations => write!(f, "iteration count must be non-zero"),
            BitflowError::InvalidTrials => write!(f, "trial count must be non-zero"),
            BitflowError::InvalidFlipCount => {
                write!(f, "max partitions to flip must be non-zero")
            }
            BitflowError::InvalidPowModulus => {
                write!(f, "pow-mod modulus must be non-zero")
            }
            BitflowError::MessageBitWidthMismatch => {
                write!(f, "message bit width does not match config")
            }
        }
    }
}

impl std::error::Error for BitflowError {}

/// Progression model for partition sizes.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum PartitionProgression {
    /// Keep a fixed partition size for every iteration.
    Fixed { size: usize },
    /// Increase partition size linearly each iteration.
    Linear { start: usize, step: usize },
    /// Increase partition size geometrically each iteration.
    Geometric { start: usize, factor: usize },
    /// Use a repeating sequence of explicit partition sizes.
    Sequence { sizes: Vec<usize> },
}

/// Configuration for generating bitflow candidates.
#[derive(Debug, Clone)]
pub struct BitflowConfig {
    pub bit_width: usize,
    pub min_partition_size: usize,
    pub max_partition_size: usize,
    pub progression: PartitionProgression,
    pub max_iterations: usize,
    pub max_partitions_to_flip: usize,
    pub per_candidate_trials: usize,
    pub seed: u64,
    pub pow_mod_base: u64,
    pub pow_mod_modulus: u64,
}

/// Represents a contiguous bit partition.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct BitPartition {
    pub start: usize,
    pub end: usize,
}

impl BitPartition {
    /// Returns the length of the partition.
    ///
    /// # Parameters
    /// - None.
    ///
    /// # Returns
    /// - `usize`: Number of bits in the partition.
    ///
    /// # Expected Output
    /// - Returns the partition length; no side effects.
    pub fn len(&self) -> usize {
        self.end.saturating_sub(self.start)
    }
}

/// Candidate generated by bitflow with inversion metadata.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct BitflowCandidate {
    pub iteration: usize,
    pub trial: usize,
    pub partition_size: usize,
    pub inverted_partitions: Vec<usize>,
    pub bits: Vec<bool>,
}

/// Generator for partitioned, inverted bitflow candidates.
#[derive(Debug)]
pub struct BitflowEngine {
    config: BitflowConfig,
    rng: StdRng,
}

impl BitflowEngine {
    /// Creates a bitflow engine with the provided configuration.
    ///
    /// # Parameters
    /// - `config`: Configuration describing partition progression and inversion behavior.
    ///
    /// # Returns
    /// - `Result<BitflowEngine, BitflowError>`: Constructed engine or validation error.
    ///
    /// # Expected Output
    /// - Returns a configured engine; no stdout/stderr output.
    pub fn new(config: BitflowConfig) -> Result<Self, BitflowError> {
        validate_config(&config)?;
        let rng = StdRng::seed_from_u64(config.seed);
        Ok(Self { config, rng })
    }

    /// Returns a reference to the configuration.
    ///
    /// # Parameters
    /// - None.
    ///
    /// # Returns
    /// - `&BitflowConfig`: Configuration reference.
    ///
    /// # Expected Output
    /// - Returns a reference; no side effects.
    pub fn config(&self) -> &BitflowConfig {
        &self.config
    }

    /// Builds the partition list for a given iteration.
    ///
    /// # Parameters
    /// - `iteration`: Zero-based iteration index.
    ///
    /// # Returns
    /// - `Result<Vec<BitPartition>, BitflowError>`: Partition list for the iteration.
    ///
    /// # Expected Output
    /// - Returns partitions; no stdout/stderr output.
    pub fn partitions_for_iteration(
        &self,
        iteration: usize,
    ) -> Result<Vec<BitPartition>, BitflowError> {
        let partition_size = partition_size_for_iteration(
            &self.config.progression,
            iteration,
            self.config.min_partition_size,
            self.config.max_partition_size,
            self.config.bit_width,
        )?;
        Ok(build_partitions(self.config.bit_width, partition_size))
    }

    /// Generates bitflow candidates from an input message.
    ///
    /// # Parameters
    /// - `message_bits`: Bit vector representing the speculative message output.
    ///
    /// # Returns
    /// - `Result<Vec<BitflowCandidate>, BitflowError>`: Candidate list with metadata.
    ///
    /// # Expected Output
    /// - Returns generated candidates; no stdout/stderr output.
    pub fn generate_candidates(
        &mut self,
        message_bits: &[bool],
    ) -> Result<Vec<BitflowCandidate>, BitflowError> {
        if message_bits.len() != self.config.bit_width {
            return Err(BitflowError::MessageBitWidthMismatch);
        }
        let mut candidates = Vec::new();
        for iteration in 0..self.config.max_iterations {
            let partition_size = partition_size_for_iteration(
                &self.config.progression,
                iteration,
                self.config.min_partition_size,
                self.config.max_partition_size,
                self.config.bit_width,
            )?;
            let partitions = build_partitions(self.config.bit_width, partition_size);
            if partitions.is_empty() {
                continue;
            }
            let flip_limit = self.config.max_partitions_to_flip.min(partitions.len());
            let pow_mod_value = mod_pow(
                self.config.pow_mod_base,
                (iteration as u64).saturating_add(1),
                self.config.pow_mod_modulus,
            );
            for trial in 0..self.config.per_candidate_trials {
                let mut indices: Vec<usize> = (0..partitions.len()).collect();
                indices.shuffle(&mut self.rng);
                let flip_count = 1 + ((pow_mod_value + trial as u64) % flip_limit as u64) as usize;
                let mut inverted_partitions: Vec<usize> =
                    indices.into_iter().take(flip_count).collect();
                inverted_partitions.sort_unstable();
                let bits =
                    apply_partition_inversions(message_bits, &partitions, &inverted_partitions);
                candidates.push(BitflowCandidate {
                    iteration,
                    trial,
                    partition_size,
                    inverted_partitions,
                    bits,
                });
            }
        }
        Ok(candidates)
    }
}

/// Validates bitflow configuration values.
///
/// # Parameters
/// - `config`: Configuration to validate.
///
/// # Returns
/// - `Result<(), BitflowError>`: `Ok(())` on success.
///
/// # Expected Output
/// - Returns validation status; no stdout/stderr output.
fn validate_config(config: &BitflowConfig) -> Result<(), BitflowError> {
    if config.bit_width == 0 {
        return Err(BitflowError::InvalidBitWidth);
    }
    if config.min_partition_size == 0
        || config.max_partition_size == 0
        || config.min_partition_size > config.max_partition_size
        || config.max_partition_size > config.bit_width
    {
        return Err(BitflowError::InvalidPartitionRange);
    }
    if config.max_iterations == 0 {
        return Err(BitflowError::InvalidIterations);
    }
    if config.per_candidate_trials == 0 {
        return Err(BitflowError::InvalidTrials);
    }
    if config.max_partitions_to_flip == 0 {
        return Err(BitflowError::InvalidFlipCount);
    }
    if config.pow_mod_modulus == 0 {
        return Err(BitflowError::InvalidPowModulus);
    }
    validate_progression(&config.progression, config.bit_width)?;
    Ok(())
}

/// Validates partition progression settings.
///
/// # Parameters
/// - `progression`: Partition progression to validate.
/// - `bit_width`: Bit width used to cap partition sizes.
///
/// # Returns
/// - `Result<(), BitflowError>`: `Ok(())` on success.
///
/// # Expected Output
/// - Returns validation status; no stdout/stderr output.
fn validate_progression(
    progression: &PartitionProgression,
    bit_width: usize,
) -> Result<(), BitflowError> {
    match progression {
        PartitionProgression::Fixed { size } => {
            if *size == 0 || *size > bit_width {
                return Err(BitflowError::InvalidPartitionRange);
            }
        }
        PartitionProgression::Linear { start, step: _ } => {
            if *start == 0 || *start > bit_width {
                return Err(BitflowError::InvalidPartitionRange);
            }
        }
        PartitionProgression::Geometric { start, factor } => {
            if *start == 0 || *start > bit_width || *factor == 0 {
                return Err(BitflowError::InvalidPartitionRange);
            }
        }
        PartitionProgression::Sequence { sizes } => {
            if sizes.is_empty() || sizes.iter().any(|size| *size == 0 || *size > bit_width) {
                return Err(BitflowError::InvalidPartitionRange);
            }
        }
    }
    Ok(())
}

/// Computes the partition size for a given iteration.
///
/// # Parameters
/// - `progression`: Partition size progression model.
/// - `iteration`: Zero-based iteration index.
/// - `min_size`: Minimum allowable partition size.
/// - `max_size`: Maximum allowable partition size.
/// - `bit_width`: Bit width used as a ceiling.
///
/// # Returns
/// - `Result<usize, BitflowError>`: Partition size for the iteration.
///
/// # Expected Output
/// - Returns the partition size; no stdout/stderr output.
fn partition_size_for_iteration(
    progression: &PartitionProgression,
    iteration: usize,
    min_size: usize,
    max_size: usize,
    bit_width: usize,
) -> Result<usize, BitflowError> {
    let raw_size = match progression {
        PartitionProgression::Fixed { size } => *size,
        PartitionProgression::Linear { start, step } => {
            start.saturating_add(step.saturating_mul(iteration))
        }
        PartitionProgression::Geometric { start, factor } => {
            let mut size = *start;
            for _ in 0..iteration {
                size = size.saturating_mul(*factor);
            }
            size
        }
        PartitionProgression::Sequence { sizes } => sizes[iteration % sizes.len()],
    };
    let clamped = raw_size.clamp(min_size, max_size).min(bit_width);
    if clamped == 0 {
        return Err(BitflowError::InvalidPartitionRange);
    }
    Ok(clamped)
}

/// Builds contiguous partitions of a bit vector.
///
/// # Parameters
/// - `bit_width`: Total number of bits.
/// - `partition_size`: Requested size for each partition.
///
/// # Returns
/// - `Vec<BitPartition>`: Contiguous partitions covering the bit width.
///
/// # Expected Output
/// - Returns partitions; no stdout/stderr output.
fn build_partitions(bit_width: usize, partition_size: usize) -> Vec<BitPartition> {
    WindowPartitionSet::build(bit_width)
        .min_width(partition_size)
        .max_width(partition_size)
        .disallow_overlap()
        .contiguous()
        .generate()
        .expect("validated bitflow partition size should produce contiguous windows")
        .partitions()
        .iter()
        .map(|partition| BitPartition {
            start: partition.index(),
            end: partition.end_exclusive(),
        })
        .collect()
}

/// Applies partition-level inversions to a bit vector.
///
/// # Parameters
/// - `message_bits`: Input bit slice to transform.
/// - `partitions`: Partition list describing segment boundaries.
/// - `inverted_partitions`: Partition indices to invert.
///
/// # Returns
/// - `Vec<bool>`: New bit vector with specified partitions inverted.
///
/// # Expected Output
/// - Returns a transformed bit vector; no stdout/stderr output.
fn apply_partition_inversions(
    message_bits: &[bool],
    partitions: &[BitPartition],
    inverted_partitions: &[usize],
) -> Vec<bool> {
    let mut output = message_bits.to_vec();
    for &partition_idx in inverted_partitions {
        if let Some(partition) = partitions.get(partition_idx) {
            for bit in &mut output[partition.start..partition.end] {
                *bit = !*bit;
            }
        }
    }
    output
}

/// Computes modular exponentiation for scheduling values.
///
/// # Parameters
/// - `base`: Base value `c`.
/// - `exponent`: Exponent `x`.
/// - `modulus`: Modulus `N`.
///
/// # Returns
/// - `u64`: `base^exponent mod modulus`.
///
/// # Expected Output
/// - Returns the modular exponentiation result; no stdout/stderr output.
fn mod_pow(base: u64, exponent: u64, modulus: u64) -> u64 {
    let mut result = 1u64 % modulus;
    let mut base_acc = base % modulus;
    let mut exp = exponent;
    while exp > 0 {
        if exp & 1 == 1 {
            result = result.saturating_mul(base_acc) % modulus;
        }
        exp >>= 1;
        base_acc = base_acc.saturating_mul(base_acc) % modulus;
    }
    result
}

#[cfg(test)]
